// Create the addition function
function add(a, b) {
    return a + b;
}

// Create the subtraction function
function subtract(a, b) {
    return a - b;
}

// Create the multiply function
function multiply(a, b) {
    return a * b;
}

// Create the division function
function divide(a, b) {
    return a / b;
}

console.log(add(85, 15));
console.log(subtract(85, 15));
console.log(multiply(85, 15));
console.log(divide(85, 15));
